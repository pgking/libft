#include "libft.h"

int		ft_strcmp(const char *str1, const char *str2)
{
	int				i;
	unsigned char	*ptr1;
	unsigned char	*ptr2;

	i = 0;
	ptr1 = (unsigned char *)str1;
	ptr2 = (unsigned char *)str2;
	while (ptr1[i] != '\0' && ptr2[i] != '\0' && ptr1[i] == ptr2[i])
		i++;
	return ((ptr1[i] - ptr2[i]));
}
