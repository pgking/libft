#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int		i;
	char				*ptr;

	i = 0;
	ptr = (char *)s;
	while (i < n)
	{
		ptr[i] = '\0';
		i++;
	}
}
