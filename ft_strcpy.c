#include "libft.h"

char	*ft_strcpy(char *str2, const char *str1)
{
	int		i;

	i = 0;
	while (str1[i])
	{
		str2[i] = str1[i];
		i++;
	}
	str2[i] = '\0';
	return (str2);
}
